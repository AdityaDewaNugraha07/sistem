    public function actionMonitoringOrder()
    {
        $model = new \app\models\TOpExport();
        $model->year = date("Y");
        if (\Yii::$app->request->isAjax) {
            $form_params = [];
            parse_str(\Yii::$app->request->post('formData'), $form_params);
            $data['html'] = "";
            if (isset($form_params['TOpExport'])) {
                $tgl_awal = "";
                $tgl_akhir = "";
                if (!empty($form_params['TOpExport']['year'])) {
                    $tgl_awal = $form_params['TOpExport']['year'] . "-01-01";
                    $tgl_akhir = $form_params['TOpExport']['year'] . "-12-31";
                }
                $sqlop = "SELECT *,m_customer.cust_an_nama FROM t_op_export 
						JOIN m_customer ON m_customer.cust_id = t_op_export.cust_id
						WHERE cancel_transaksi_id IS NULL 
						" . ((!empty($tgl_awal) && !empty($tgl_akhir)) ? "AND tanggal BETWEEN '" . $tgl_awal . "' AND '" . $tgl_akhir . "'" : "") . " 
						" . (!empty($form_params['TOpExport']['cust_id']) ? "AND t_op_export.cust_id = " . $form_params['TOpExport']['cust_id'] : "") . " 
						ORDER BY tanggal DESC";
                $modOPs = \Yii::$app->db->createCommand($sqlop)->queryAll();
                if (count($modOPs) > 0) {
                    foreach ($modOPs as $i => $op) {
                        $sqldetail = "SELECT detail_order FROM t_op_export where op_export_id = " . $op['op_export_id'];
                        $modDetails = \Yii::$app->db->createCommand($sqldetail)->queryOne();
                        if (count($modDetails) > 0) {
                            $modDetails = \yii\helpers\Json::decode($modDetails['detail_order']);
                            foreach ($modDetails as $ii => $detail) {
                                $data['html'] .= $this->renderPartial('monitoringOrder/_item', [
                                    'op' => $op,
                                    'detail' => $detail,
                                    'modOPs' => $modOPs,
                                    'i' => $i, 'ii' => $ii
                                ]);
                            }
                        }
                    }
                }
            }
            return $this->asJson($data);
        }
        return $this->render('/laporan/monitoringOrder/index', ['model' => $model]);
    }

