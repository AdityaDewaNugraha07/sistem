    public function actionOpexportblmproformaprint()
    {
        $this->layout       = '@views/layouts/metronic/print';
        $caraprint          = Yii::$app->request->get('caraprint');
        $paramprint['judul']= Yii::t('app', 'Laporan OP Export belum Proforma Packing List');
        $model              = new \app\models\TOpExport();
        $request            = Yii::$app->request->get('TOpExport');
        $model->attributes  = $request;
        $model->tgl_awal    = \app\components\DeltaFormatter::formatDateTimeForDb($request['tgl_awal']);
        $model->tgl_akhir   = \app\components\DeltaFormatter::formatDateTimeForDb($request['tgl_akhir']);
        $model->jns_produk  = $request['jns_produk'];

        $param              = $this->_query_op_export_blm_proforma($model);

        $query              = "SELECT ";
        foreach($param['column'] as $key => $col) {
            if($key < count($param['column']) - 1) {
                $query .= $col . ", ";
            }else {
                $query .= $col;
            }
        }
        $query .= " FROM {$param['table']} ";
        foreach($param['join'] as $join) {
            $query .= $join;
        }
        $query .= " WHERE {$param['where']} ";

        $model  = Yii::$app->db->createCommand($query)->queryAll();
        if($caraprint === 'EXCEL') {
            return $this->renderPartial('/laporan/opexportblmproforma/print', compact('model', 'paramprint'));
        }
    }

    private function _query_op_export_blm_proforma($model)
    {
        $param = [
            'table'     => 't_op_export',
            'pk'        => 't_op_export.op_export_id',
            'column'    => [
                't_op_export.op_export_id',
                't_op_export.kode',
                't_op_export.nomor_kontrak',
                't_op_export.jenis_produk',
                't_op_export.tanggal',
                'applicant.cust_an_nama',
                'applicant.cust_an_alamat',
                'notify.cust_an_nama AS notify_nama',
                'notify.cust_an_alamat AS notify_alamat',
                't_op_export.payment_method',
                't_op_export.term_of_price',
                't_op_export.hs_code',
                't_op_export.svlk_no',
                't_op_export.cancel_transaksi_id',
                't_cancel_transaksi.cancel_reason'
            ],
            'join'      => ['
                LEFT JOIN m_customer AS applicant ON applicant.cust_id = t_op_export.cust_id
                LEFT JOIN m_customer AS notify ON notify.cust_id = t_op_export.notify_party
                LEFT JOIN t_packinglist ON t_packinglist.op_export_id = t_op_export.op_export_id
                LEFT JOIN t_cancel_transaksi ON t_cancel_transaksi.cancel_transaksi_id = t_op_export.cancel_transaksi_id
            '],
            'where'     => " t_packinglist.op_export_id IS NULL" 
                        .  " AND ( t_op_export.tanggal BETWEEN '{$model->tgl_awal}' AND '{$model->tgl_akhir}')" 
        ];

        if(!empty($model->jns_produk)) {
            $param['where'] = $param['where'] . " AND t_op_export.jenis_produk = '{$model->jns_produk}'";
        }

        return $param;
    }
