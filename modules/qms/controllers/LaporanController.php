	public function actionDokumen()
	{
		$model = new \app\models\MDokumen();
		if (\Yii::$app->request->get('dt') == 'table-laporan') {
			if ((\Yii::$app->request->get('laporan_params')) !== null) {
				$form_params = [];
				parse_str(\Yii::$app->request->get('laporan_params'), $form_params);
				$model->attributes = $form_params['MDokumen'];
                $model->kategori_dokumen = $form_params['MDokumen']['kategori_dokumen'];
				$model->jenis_dokumen = $form_params['MDokumen']['jenis_dokumen'];
			}
			return \yii\helpers\Json::encode(\app\components\SSP::complex($model->searchLaporanDt()));
		}
		return $this->render('/laporan/dokumen/index', ['model' => $model]);
	}

