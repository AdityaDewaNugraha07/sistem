	public function actionIndukdokumenPrint()
	{
		$this->layout = '@views/layouts/metronic/print';
		$model = new \app\models\MDokumen();
		$departement_id = Yii::$app->user->identity->pegawai->departement_id;
		$modDep = \app\models\MDepartement::findOne($departement_id);
		$caraprint = Yii::$app->request->get('caraprint');
		$paramprint['judul'] = Yii::t('app', 'Daftar Induk Dokumen');
		$paramprint['judul2'] = "Bagian : " . $modDep->departement_nama ."<br>";
		$maxTanggal = $model->searchLaporanInduk()
						->select('MAX(t_dokumen_revisi.tanggal_berlaku) AS max_tanggal')
						->groupBy('m_dokumen.dokumen_id')
						->orderBy(['max_tanggal' => SORT_DESC])
						->asArray()
						->all();
		if($maxTanggal){
			$max_tgl = $maxTanggal[0]['max_tanggal'];
		} else {
			$max_tgl = date('Y-m-d');
		}
		$paramprint['judul2'] .= 'Update : '.\app\components\DeltaFormatter::formatDateTimeId($max_tgl);
		// $model->attributes = $_GET['MDokumen'];

		if ($caraprint == 'PRINT') {
			return $this->render('/laporan/indukdokumen/print', ['model' => $model, 'paramprint' => $paramprint]);
		} else if ($caraprint == 'PDF') {
			$pdf = Yii::$app->pdf;
			$pdf->options = ['title' => $paramprint['judul']];
			$pdf->filename = $paramprint['judul'] . '.pdf';
