    public function actionTerimaBhpTotal()
    {
        $model = new \app\models\ViewTerimaBhpDetail();
        $param = Yii::$app->request->get('ViewTerimaBhpDetail');
        $model->attributes              = $param;
        $model->tgl_awal                = $param['tgl_awal'];
        $model->tgl_akhir               = $param['tgl_akhir'];
        $model->terimabhp_kode          = $param['terimabhp_kode'];
        $model->bhp_nm                  = $param['bhp_nm'];
        $model->suplier_id              = $param['suplier_id'];
        $model->terimabhpd_keterangan   = $param['terimabhpd_keterangan'];
        $total  = 0;
        foreach($model->searchLaporan()->createCommand()->query() as $row) {
            // $total += $row['total'];
			$total += ($row['terimabhpd_qty'] * $row['terimabhpd_harga']) + $row['ppn'] + $row['pph_peritem'] + $row['total_pbbkb'];
        }
        return Json::encode(['total' => $total]);
    }

