	public function actionSuplierPrint(){
		$this->layout = '@views/layouts/metronic/print';
		$model = new \app\models\MSuplier();
		$caraprint = Yii::$app->request->get('caraprint');
		$model->attributes = $_GET['MSuplier'];
		$model->tgl_awal = !empty($_GET['MSuplier']['tgl_awal'])? DeltaFormatter::formatDateTimeForDb($_GET['MSuplier']['tgl_awal']):"";
		$model->tgl_akhir = !empty($_GET['MSuplier']['tgl_akhir'])? DeltaFormatter::formatDateTimeForDb($_GET['MSuplier']['tgl_akhir']):"";
		$model->suplier_nm = $_GET['MSuplier']['suplier_nm'];
		$model->type = $_GET['MSuplier']['type'];
		$model->suplier_nm_company = $_GET['MSuplier']['suplier_nm_company'];
		$model->suplier_almt = $_GET['MSuplier']['suplier_almt'];
		$paramprint['judul'] = Yii::t('app', 'Laporan Suplier');
		if((!empty($model->tgl_awal)) && (!empty($model->tgl_akhir))){
			$paramprint['judul2'] = "Periode Tanggal ". DeltaFormatter::formatDateTimeForUser($model->tgl_awal)." sd ". DeltaFormatter::formatDateTimeForUser($model->tgl_akhir);
		}
		if($caraprint == 'PRINT'){
			return $this->renderPartial('/laporan/suplier/print',['model'=>$model,'paramprint'=>$paramprint]);
		}else if($caraprint == 'PDF'){
			$pdf = Yii::$app->pdf;
			$pdf->options = ['title' => $paramprint['judul']];
			$pdf->filename = $paramprint['judul'].'.pdf';
			$pdf->methods['SetHeader'] = ['Generated By: '.Yii::$app->user->getIdentity()->userProfile->fullname.'||Generated At: ' . date('d/m/Y H:i:s')];
			$pdf->content = $this->renderPartial('/laporan/suplier/print',['model'=>$model,'paramprint'=>$paramprint]);
			return $pdf->render();
		}else if($caraprint == 'EXCEL'){
			return $this->renderPartial('/laporan/suplier/print',['model'=>$model,'paramprint'=>$paramprint]);
		}
	}
