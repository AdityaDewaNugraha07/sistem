	public function actionInfo($id,$sppdid)
	{
		if (\Yii::$app->request->isAjax) {
			$model = \app\models\MapSppDetailReff::findOne(['reff_detail_id'=>$id,'sppd_id'=>$sppdid]);
			
			$ex = substr($model->reff_no, 0,3);			
			if($ex == "SPO"){				
				$modSPO = \app\models\TSpo::findOne(['spo_kode'=>$model->reff_no]);
				$modelDetail = \app\models\TSpoDetail::findOne(['spod_id'=>$id]);			
				$renData = $this->renderAjax('/laporan/monitoringSpp/infoSPO', ['spo_id'=>$modSPO->spo_id,'bhp_id' => $modelDetail->bhp_id, 'id' => $id, 'model' => $model]);
			}elseif($ex == "SPL"){
				$modSPL = \app\models\TSpl::findOne(['spl_kode'=>$model->reff_no]);
				$modelDetail = \app\models\TSplDetail::findOne(['spld_id'=>$id]);
				$renData = $this->renderAjax('/laporan/monitoringSpp/infoSPL', ['spl_id'=>$modSPL->spl_id,'bhp_id' => $modelDetail->bhp_id, 'id' => $id, 'model' => $model]);
			}
			return $renData ;
		}
	}

