                't_spp_detail.status_closed IS NULL ',
                't_spp_detail.sppd_id NOT IN ( SELECT map_spp_detail_reff.sppd_id FROM map_spp_detail_reff WHERE map_spp_detail_reff.terima_bhpd_id IS NOT NULL ) ',
                't_spp.cancel_transaksi_id IS NULL '
            ];

            if(isset($filter['tgl_awal'], $filter['tgl_akhir']) && !empty($filter['tgl_awal']) && !empty($filter['tgl_akhir'])) {
                $param['where'][] = "t_spp.spp_tanggal BETWEEN '{$filter['tgl_awal']}' AND '{$filter['tgl_akhir']}' ";
            }

