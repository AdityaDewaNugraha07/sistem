	public function actionStockPrint(){
        $this->layout = '@views/layouts/metronic/print';
        $model = new HPersediaanProduk();
        $caraprint = Yii::$app->request->get('caraprint');
        $model->attributes      = $_GET['HPersediaanProduk'];
        $model->produk_nama     = $_GET['HPersediaanProduk']['produk_nama'];
        $model->produk_group    = $_GET['HPersediaanProduk']['produk_group'];
        $model->per_tanggal     = $_GET['HPersediaanProduk']['per_tanggal'];
        $paramprint['judul']    = Yii::t('app', 'Laporan Persediaan Produk');
        $paramprint['judul2']   = "Per Tanggal " . DeltaFormatter::formatDateTimeForUser2(!empty($model->per_tanggal) ? $model->per_tanggal : date('d/m/Y'));
        
        if ($caraprint === 'PRINT') {
            return $this->renderPartial('/availablestockproduk/print', ['model' => $model, 'paramprint' => $paramprint]);
        }

        if ($caraprint === 'PDF') {
            $pdf = Yii::$app->pdf;
            $pdf->options = ['title' => $paramprint['judul']];
            $pdf->filename = $paramprint['judul'] . '.pdf';
            $pdf->methods['SetHeader'] = ['Generated By: ' . Yii::$app->user->getIdentity()->userProfile->fullname . '||Generated At: ' . date('d/m/Y H:i:s')];
            $pdf->content = $this->renderPartial('/availablestockproduk/print', ['model' => $model, 'paramprint' => $paramprint]);
            return $pdf->render();
        }

        if ($caraprint === 'EXCEL') {
            return $this->renderPartial('/availablestockproduk/print', ['model' => $model, 'paramprint' => $paramprint]);
        }
    }
	
