	public function actionProduklist($produk_id=null,$satuan_kecil=null,$gudang_nm=null){
		if(\Yii::$app->request->isAjax){
			if(\Yii::$app->request->get('dt') === 'table-produk'){
				$param['table']= HPersediaanProduk::tableName();
				$param['pk']= HPersediaanProduk::primaryKey()[0];
				$param['column'] = [$param['table'].'.produk_id',
									'm_brg_produk.produk_kode',
									'm_brg_produk.produk_nama',
									$param['table'].'.nomor_produksi',
									't_produksi.tanggal_produksi',
									't_produksi.plymill_shift',
									't_produksi.sawmill_line',
									'm_gudang.gudang_nm',
									'sum(in_qty_kecil-out_qty_kecil) AS qty_kecil',
									'in_qty_kecil_satuan',
									'sum(in_qty_m3-out_qty_m3) AS kubikasi',
									];
				$param['group'] = 'GROUP BY '.$param['table'].'.produk_id, 
									 m_brg_produk.produk_kode, 
									 m_brg_produk.produk_nama, 
									 in_qty_kecil_satuan,
									 '.$param['table'].'.nomor_produksi,
									 t_produksi.tanggal_produksi,
									 t_produksi.plymill_shift,
									 t_produksi.sawmill_line,
									 m_gudang.gudang_nm,
									 in_qty_kecil_satuan
									 ';
				$param['join'] = ['JOIN m_brg_produk ON m_brg_produk.produk_id = h_persediaan_produk.produk_id
								  JOIN t_produksi ON t_produksi.nomor_produksi = '.$param['table'].'.nomor_produksi
								  JOIN m_gudang ON m_gudang.gudang_id = '.$param['table'].'.gudang_id'];
				if(!empty($produk_id)){
					$param['where'] = $param['table'].".produk_id = ".$produk_id;
				}
				if(!empty($produk_id) && !empty($satuan_kecil) && !empty($gudang_nm)){
					$param['where'] .= " AND in_qty_kecil_satuan = '".$satuan_kecil."' AND gudang_nm = '".$gudang_nm."'";
				}
				$param['having'] = "HAVING SUM(in_qty_kecil-out_qty_kecil) > 0";
				return Json::encode(SSP::complex( $param ));
			}
			return $this->renderAjax('produklist',['produk_id'=>$produk_id,'satuan_kecil'=>$satuan_kecil,'gudang_nm'=>$gudang_nm]);
        }
    }
	
	public function actionProdukListOnModal($tr_seq=null,$jns_produk=null,$notin=null){
		if(\Yii::$app->request->isAjax){
			if(\Yii::$app->request->get('dt')=='table-produk'){
				$notinpost = json_decode( Yii::$app->request->get('notin') );
				$notin = "";
				if(!empty($notinpost)){
					$notin = "AND h_persediaan_produk.nomor_produksi NOT IN(";
					foreach($notinpost as $i => $not){
						$notin .= "'$not'";
						if( ($i+1)!=(count($notinpost)) ){
							$notin .= ",";
						}
					}
					$notin .= ")";
				}
				$param['table']= HPersediaanProduk::tableName();
				$param['pk']= HPersediaanProduk::primaryKey()[0];
				$param['column'] = [$param['table'].'.produk_id',
									$param['table'].'.nomor_produksi',
									'm_brg_produk.produk_kode',
									'm_brg_produk.produk_nama',
									't_produksi.tanggal_produksi',
									't_produksi.plymill_shift',
									't_produksi.sawmill_line',
									'm_gudang.gudang_nm',
									'sum(in_qty_palet-out_qty_palet) AS qty_palet',
									'sum(in_qty_kecil-out_qty_kecil) AS qty_kecil',
									'in_qty_kecil_satuan',
									'sum(in_qty_m3-out_qty_m3) AS kubikasi',
									$param['table'].'.gudang_id',
									];
				$param['group'] = 'GROUP BY '.$param['table'].'.produk_id, 
									'.$param['table'].'.nomor_produksi,
									 m_brg_produk.produk_kode, 
									 m_brg_produk.produk_nama, 
									 in_qty_kecil_satuan,
									 t_produksi.tanggal_produksi,
									 t_produksi.plymill_shift,
									 t_produksi.sawmill_line,
									 m_gudang.gudang_nm,
									 in_qty_kecil_satuan,
									 '.$param['table'].'.gudang_id
									 ';
				$param['join'] = ['JOIN m_brg_produk ON m_brg_produk.produk_id = h_persediaan_produk.produk_id
								  JOIN t_produksi ON t_produksi.nomor_produksi = '.$param['table'].'.nomor_produksi
								  JOIN m_gudang ON m_gudang.gudang_id = '.$param['table'].'.gudang_id'];
				$param['where'] = "m_brg_produk.produk_group = '".$jns_produk."' ".$notin;
				$param['having'] = "HAVING SUM(in_qty_palet-out_qty_palet) > 0";
				return Json::encode(SSP::complex( $param ));
			}
			return $this->renderAjax('listOnModal',['tr_seq'=>$tr_seq,'jns_produk'=>$jns_produk,'notin'=>$notin]);
		}
	}
	
