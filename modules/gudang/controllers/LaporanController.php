    public function actionDetailRandom() {
		if(\Yii::$app->request->isAjax){
            $nomor_produksi = Yii::$app->request->get('nomor_produksi');
            $tgl_awal = Yii::$app->request->get('tgl_awal');
            $tgl_akhir = Yii::$app->request->get('tgl_akhir');

            $sql_nomor_dokumen = "select b.nomor_dokumen
                                    from t_dokumen_penjualan_detail a
                                    join t_dokumen_penjualan b on b.dokumen_penjualan_id = a.dokumen_penjualan_id 
                                    join t_spm_ko_detail c on c.spm_kod_id = a.spm_kod_id
                                    join t_spm_ko d on d.spm_ko_id = c.spm_ko_id
                                    join t_op_ko_random e on e.nomor_produksi = '".$nomor_produksi."'
                                    where e.nomor_produksi = '".$nomor_produksi."'
                                    ";
            $nomor_dokumen = Yii::$app->db->createCommand($sql_nomor_dokumen)->queryScalar();

            if (!empty($nomor_dokumen)) {
                $sql = "select d.produk_nama, b.tanggal, a.reff_no, b.cara_keluar,
                            c.qty_kecil, c.t, c.t_satuan, c.l, c.l_satuan, c.p, c.p_satuan, c.p, c.kubikasi, 
                            f.cust_an_nama
                        from h_persediaan_produk a
                        join t_produk_keluar b on b.nomor_produksi =  '".$nomor_produksi."'
                        join t_op_ko_random c on c.nomor_produksi =   '".$nomor_produksi."'
                        join m_brg_produk d on d.produk_id = a.produk_id                    
                        join t_spm_ko e on e.kode = b.reff_no
                        join m_customer f on f.cust_id = e.cust_id
                        where a.nomor_produksi = '".$nomor_produksi."'
                            and a.reff_no not ilike 'TKO%' 
                            and a.keterangan NOT ILIKE '%MUTASI DARI GUDANG%' 
                            and tgl_transaksi between '".$tgl_awal."' and '".$tgl_akhir."'
                        group by d.produk_nama, b.tanggal, a.reff_no, b.cara_keluar,
                            c.qty_kecil, c.t, c.t_satuan, c.l, c.l_satuan, c.p, c.p_satuan, c.p, c.kubikasi, 
                            f.cust_an_nama
                        ";
            } else {
                $sql = "select e.produk_nama, a.tgl_transaksi as tanggal, a.reff_no, a.keterangan as cara_keluar,
                            d.qty as qty_kecil, d.t, d.t_satuan, d.l, d.l_satuan, d.p, d.p_satuan, d.p, d.kapasitas_kubikasi as kubikasi, 
