                        join t_terima_ko_kd d on d.tbko_id = c.tbko_id
                        join m_brg_produk e on e.produk_id = a.produk_id                    
                        where a.nomor_produksi = '".$nomor_produksi."'
                            and c.nomor_produksi = '".$nomor_produksi."'
                            and a.keterangan NOT ILIKE '%MUTASI DARI GUDANG%' 
                            and tgl_transaksi between '".$tgl_awal."' and '".$tgl_akhir."'
                        and a.out_qty_palet > 0
                        group by e.produk_nama, a.tgl_transaksi, a.reff_no, cara_keluar,
                            d.qty, d.t, d.t_satuan, d.l, d.l_satuan, d.p, d.p_satuan, d.p, d.kapasitas_kubikasi, 
                            cust_an_nama
                        ";
            }
            $list = Yii::$app->db->createCommand($sql)->queryAll();
            
