			return $this->renderAjax('/laporan/produkKeluar/_detailRandom',['sql'=>$sql, 'nomor_produksi'=>$nomor_produksi, 'nomor_dokumen'=>$nomor_dokumen, 'list'=>$list]);
        }        
    }
	
