	public function actionGetContent()
	{
		if (\Yii::$app->request->isAjax) {
			$jp = Yii::$app->request->get('jp');
			$kode = Yii::$app->request->get('kode');

			$sql_tanggal = "select harga_tanggal_penetapan from m_harga_produk where kode = '" . $kode . "' ";
			$harga_tanggal_penetapan = Yii::$app->db->createCommand($sql_tanggal)->queryScalar();

			$sql = " select b.kode, b.harga_tanggal_penetapan, a.produk_id, a.produk_kode, a.produk_nama, a.produk_dimensi, b.harga_enduser " .
				"   from m_brg_produk a " .
				"   left join m_harga_produk b on b.produk_id = a.produk_id " .
				"   where a.produk_group = '" . $jp . "'  " .
				//"   and a.active = TRUE ".
				//"	and b.harga_tanggal_penetapan = '".$tp."' ".
				"   and b.kode = '" . $kode . "' " .
				"   group by b.kode, b.harga_tanggal_penetapan, a.produk_id, a.produk_kode, a.produk_nama, b.harga_enduser " .
				"   order by a.produk_id asc " .
				"   ";

			$models = Yii::$app->db->createCommand($sql)->queryAll();
			$numrows = count($models);

			if ($numrows > 0) {
				return $this->renderAjax('/laporan/pricelist/_content', ['numrows' => $numrows, 'models' => $models, 'jp' => $jp, 'kode' => $kode, 'harga_tanggal_penetapan' => $harga_tanggal_penetapan]);
			} else {
				return $this->renderAjax('/laporan/pricelist/_content', ['numrows' => $numrows, 'sql' => $sql]);
			}
		}
	}

