				"   left join m_harga_log b on b.log_id = a.log_id " .
				"   and b.kode = '" . $kode . "' " .
				"   group by b.kode, b.harga_tanggal_penetapan, a.log_id, a.log_kode, a.log_nama, b.harga_enduser " .
