		if(!empty($model->tgl_transaksi)){
			$paramprint['judul2'] = "Per Tanggal ". \app\components\DeltaFormatter::formatDateTimeForDb($model->tgl_transaksi);
		}
		if($caraprint == 'PRINT'){
