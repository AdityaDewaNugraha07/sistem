	}
	
	public function actionPersediaanBhpPrint(){
		$this->layout = '@views/layouts/metronic/print';
		$model = new \app\models\HPersediaanBhp();
		$caraprint = Yii::$app->request->get('caraprint');
		$model->tgl_transaksi = (isset($_GET['HPersediaanBhp']['tgl_transaksi'])?$_GET['HPersediaanBhp']['tgl_transaksi']:"");
