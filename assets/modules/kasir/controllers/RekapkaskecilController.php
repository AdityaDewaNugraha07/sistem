				$model = new \app\models\TUangtunai();
				if(!empty($model)){
					return $this->renderAjax('uangtunai',['model'=>$model,'tgl'=>$tgl,'info'=>true]);
				}
			}
			if($info=='kasbon'){
				$models = \app\models\HBonsementara::find()->where("tanggal = '".$tgl."' AND tipe = 'KK'")->orderBy(['bonsementara_id'=>SORT_ASC])->all();;
				if(!empty($models)){
					return $this->renderAjax('@app/modules/kasir/views/pengeluarankaskecil/kasbon',['models'=>$models,'tgl'=>$tgl]);
				}
			}
			if($info=='kasbonkasbesar'){
				$models = \app\models\HBonsementara::find()->where("tanggal = '".$tgl."' AND tipe = 'KB' AND status = 'KASBON KASBESAR KE KASKECIL'")->orderBy(['bonsementara_id'=>SORT_ASC])->all();;
				if(!empty($models)){
					return $this->renderAjax('@app/modules/kasir/views/pengeluarankaskecil/kasbonkasbesar',['models'=>$models,'tgl'=>$tgl]);
				}
			}
			return false;
		}
	}
	
	
	public function actionTanggalSelisihClosing(){
		if(\Yii::$app->request->isAjax){
			$sql = "SELECT t_kas_kecil.tanggal AS tgl_Laporan, h_saldo_kaskecil.created_at::DATE AS tgl_Closing, (h_saldo_kaskecil.created_at::DATE - t_kas_kecil.tanggal) AS selisih_hari FROM t_kas_kecil 
					JOIN h_saldo_kaskecil ON h_saldo_kaskecil.reff_no = t_kas_kecil.kode
					WHERE t_kas_kecil.tanggal != h_saldo_kaskecil.created_at::DATE
					GROUP BY t_kas_kecil.tanggal, h_saldo_kaskecil.created_at::DATE
					ORDER BY t_kas_kecil.tanggal DESC";
			$models = Yii::$app->db->createCommand($sql)->queryAll();
			return $this->renderAjax('tanggalselisihclosing',['models'=>$models]);
		}
	}
